// Helper functions for filtering stocks
export const getNifty50Stocks = () =>
  nseStocks.filter((stock) => stock.isNifty50);
export const getSensex30Stocks = () =>
  nseStocks.filter((stock) => stock.isSensex30);
export const getNiftyNext50Stocks = () =>
  nseStocks.filter((stock) => stock.isNiftyNext50);
export const getMidCapStocks = () =>
  nseStocks.filter((stock) => stock.isNiftyMidcap);
export const getSmallCapStocks = () =>
  nseStocks.filter((stock) => stock.isNiftySmallcap);

// Search function
export const searchStocks = (query: string): NSEStock[] => {
  if (!query || query.length < 2) return [];

  const searchTerm = query.toLowerCase();
  return nseStocks.filter(
    (stock) =>
      stock.symbol.toLowerCase().includes(searchTerm) ||
      stock.name.toLowerCase().includes(searchTerm) ||
      stock.sector.toLowerCase().includes(searchTerm) ||
      stock.industry.toLowerCase().includes(searchTerm)
  );
};

// Get stock by symbol
export const getStockBySymbol = (symbol: string): NSEStock | undefined => {
  return nseStocks.find((stock) => stock.symbol === symbol);
};

// Get stocks by sector
export const getStocksBySector = (sector: string): NSEStock[] => {
  return nseStocks.filter((stock) => stock.sector === sector);
};

// Get all unique sectors
export const getAllSectors = (): string[] => {
  return [...new Set(nseStocks.map((stock) => stock.sector))].sort();
};

// Get all unique industries
export const getAllIndustries = (): string[] => {
  return [...new Set(nseStocks.map((stock) => stock.industry))].sort();
};
